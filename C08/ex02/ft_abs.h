#ifndef FT_ABS_H
# define FT_ABS_H

# define ABS(nbr) ({ \
        int	r; \
        if ((nbr) < 0) \
        { \
    		r = -(nbr); \
        } \
        else \
        { \
        	r = (nbr); \
        } \
    	r; \
}) \

#endif